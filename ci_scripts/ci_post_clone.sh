#!/bin/sh

# ci_post_clone.sh
# This script runs after Xcode Cloud clones your repository
# It installs CocoaPods dependencies before building

set -e  # Exit on any error

echo "üöÄ Starting Xcode Cloud post-clone script..."
echo "================================================"

# Print environment info for debugging
echo "üìç Working directory: $(pwd)"
echo "üìç CI_WORKSPACE: ${CI_WORKSPACE}"
echo "üìç CI_PRIMARY_REPOSITORY_PATH: ${CI_PRIMARY_REPOSITORY_PATH}"
echo "üìã Node version: $(node --version || echo 'Node not found')"
echo "üìã NPM version: $(npm --version || echo 'NPM not found')"
echo "üìã Ruby version: $(ruby --version || echo 'Ruby not found')"
echo "üìã Pod version: $(pod --version || echo 'CocoaPods not found')"
echo "================================================"

# Determine the correct base path
# Xcode Cloud uses CI_WORKSPACE or we can use current directory
if [ -n "$CI_WORKSPACE" ]; then
    BASE_PATH="$CI_WORKSPACE"
    echo "‚úÖ Using CI_WORKSPACE: $BASE_PATH"
elif [ -n "$CI_PRIMARY_REPOSITORY_PATH" ]; then
    BASE_PATH="$CI_PRIMARY_REPOSITORY_PATH"
    echo "‚úÖ Using CI_PRIMARY_REPOSITORY_PATH: $BASE_PATH"
else
    BASE_PATH="$(pwd)"
    echo "‚úÖ Using current directory: $BASE_PATH"
fi

cd "$BASE_PATH"
echo "üìç Changed to base path: $(pwd)"

# Install Node dependencies
if [ -f "package.json" ]; then
    echo "üì¶ Installing Node dependencies..."
    npm ci --prefer-offline --no-audit || npm install
    echo "‚úÖ Node dependencies installed"
    echo "üì¶ node_modules exists: $([ -d "node_modules" ] && echo 'YES' || echo 'NO')"
else
    echo "‚ö†Ô∏è No package.json found at $(pwd)"
fi

# Navigate to iOS App directory
echo "üìç Navigating to iOS App directory..."
cd ios/App || {
    echo "‚ùå Failed to navigate to ios/App"
    echo "üìÇ Contents of ios/: $(ls -la ../ios/ 2>&1 || echo 'ios directory not found')"
    exit 1
}

echo "üìç Now in iOS App directory: $(pwd)"
echo "üìÇ Directory contents:"
ls -la

# Check if Podfile exists
if [ ! -f "Podfile" ]; then
    echo "‚ùå Error: Podfile not found at $(pwd)"
    echo "üìÇ Directory contents:"
    ls -la
    exit 1
fi

echo "‚úÖ Podfile found!"

# Clean up any existing Pods directory to avoid conflicts
if [ -d "Pods" ]; then
    echo "üßπ Removing existing Pods directory..."
    rm -rf Pods
    rm -f Podfile.lock
fi

# Install CocoaPods dependencies
echo "üîß Installing CocoaPods dependencies..."
echo "üìã Pod version: $(pod --version)"

# Install pods with verbose output
echo "üöÄ Running pod install --repo-update --verbose..."
pod install --repo-update --verbose || {
    echo "‚ùå pod install with repo-update failed, trying without repo update..."
    echo "üöÄ Running pod install --verbose..."
    pod install --verbose || {
        echo "‚ùå pod install failed completely, trying with clean install..."
        echo "üßπ Cleaning Pods directory..."
        rm -rf Pods
        rm -f Podfile.lock
        echo "üöÄ Running pod install --clean-install..."
        pod install --clean-install --verbose
    }
}

echo "‚úÖ CocoaPods dependencies installed successfully!"

# Verify Pods directory was created
if [ -d "Pods" ]; then
    echo "‚úÖ Pods directory created successfully"
    echo "üì¶ Pods directory contents:"
    ls -la Pods/ | head -20
    
    # Check for the specific xcconfig file that was failing
    if [ -f "Pods/Target Support Files/Pods-App/Pods-App.release.xcconfig" ]; then
        echo "‚úÖ Pods-App.release.xcconfig exists!"
    else
        echo "‚ö†Ô∏è Warning: Pods-App.release.xcconfig not found"
        echo "üìÇ Target Support Files contents:"
        ls -la "Pods/Target Support Files/" 2>&1 || echo "Target Support Files not found"
    fi
    
    # Verify workspace was created
    if [ -f "App.xcworkspace" ]; then
        echo "‚úÖ App.xcworkspace created successfully!"
    else
        echo "‚ùå ERROR: App.xcworkspace not created!"
        exit 1
    fi
    
else
    echo "‚ùå ERROR: Pods directory was not created!"
    exit 1
fi

# Final verification - ensure the exact files that Xcode Cloud needs exist
echo "üîç Final verification for Xcode Cloud..."
if [ -f "Pods/Target Support Files/Pods-App/Pods-App.release.xcconfig" ]; then
    echo "‚úÖ Pods-App.release.xcconfig verified for Xcode Cloud"
else
    echo "‚ùå CRITICAL: Pods-App.release.xcconfig missing - this will cause Xcode Cloud to fail"
    exit 1
fi

if [ -f "App.xcworkspace" ]; then
    echo "‚úÖ App.xcworkspace verified for Xcode Cloud"
else
    echo "‚ùå CRITICAL: App.xcworkspace missing - this will cause Xcode Cloud to fail"
    exit 1
fi

echo "================================================"
echo "üéâ Post-clone script completed successfully!"
echo "üìã Xcode Cloud should now use App.xcworkspace"
echo "üìã All required Pods configuration files are present"
echo "================================================"

